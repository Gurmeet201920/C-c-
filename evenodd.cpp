//program for even and odd
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cout << "the number is" << endl;
    cin >> n;
    if (n % 2 == 0)
    {
        cout << "the number is even." << endl;
    }
    if (n % 2 == 1)
    {
        cout << "the number is odd." << endl;
    }
    return 0;
}
